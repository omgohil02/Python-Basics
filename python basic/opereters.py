#operaters in python

# Artithmetic Operators (+,-,*,/,%,//,**)
# comparison Operators (==,!=,>,<,>=,<=)
# logical Operators (and, or, not)

x = 10
y = 5
print ("Addition:", x + y)          # Addition (output: 15)
print ("Subtraction:", x - y)       # Subtraction (output: 5)
print ("Multiplication:", x * y)    # Multiplication (output: 50)
print ("Division:", x / y)          # Division (output: 2.0)
print ("Find Remainder:", x % y)    # Find Remainders (output: 0)
print ("Floor Division:", x // y)   # Floor Division (output: 2)
print ("Exponent:", x ** y)         # Exponent (power) (output: 100000)

a = 10
b = 10

print ("Equal:", a == b)                   # Equal (output: True)
print ("Not Equal:", a != b)               # Not Equal (output: False)
print ("Greater Than:", a > b)             # Greater Than (output: False)
print ("Less Than:", a < b)                # Less Than (output: False)
print ("Greater Than or Equal:", a >= b)   # Greater Than or Equal (output: True)
print ("Less Than or Equal:", a <= b)      # Less Than or Equal (output: True)


a = 10
