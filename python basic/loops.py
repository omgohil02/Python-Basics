#loops in python

#For Loop: Iterates over a sequence or a fixed range of numbers
#iterable = [0, 1, 2, 3, 4, 5]

for i in range(6):
    print (i)  #output: 0,1,2,3,4,5

#While Loop: Continues running as long as a chosen condition remains true
#runs While the condition is true

counter = 0
while counter < 5:
    print (counter)  #output: 0,1,2,3,4
    counter += 1
    